// Helper functions for text management
export const getRandomText = (difficulty = 'medium') => {
  const texts = SAMPLE_TEXTS[difficulty] || SAMPLE_TEXTS.medium;
  return texts[Math.floor(Math.random() * texts.length)];
};

export const getTextByIndex = (difficulty = 'medium', index = 0) => {
  const texts = SAMPLE_TEXTS[difficulty] || SAMPLE_TEXTS.medium;
  return texts[index % texts.length];
};

export const getTextCount = (difficulty = 'medium') => {
  return SAMPLE_TEXTS[difficulty]?.length || 0;
};

export const getAllDifficulties = () => {
  return Object.keys(SAMPLE_TEXTS);
};

// Text statistics for difficulty assessment
export const getTextStats = (text) => {
  const chars = text.length;
  const words = text.split(/\s+/).filter(word => word.length > 0).length;
  const sentences = text.split(/[.!?]+/).filter(s => s.trim().length > 0).length;
  const avgWordLength = words > 0 ? Math.round(chars / words) : 0;

  // Estimate reading time (words per minute)
  const wpm = 200; // Average reading speed
  const readingTimeMinutes = words / wpm;

  return {
    chars,
    words,
    sentences,
    avgWordLength,
    estimatedReadingTime: Math.max(1, Math.round(readingTimeMinutes * 60)), // in seconds
  };
};

// Difficulty assessment based on text characteristics
export const assessDifficulty = (text) => {
  const stats = getTextStats(text);
  const complexityScore =
    (stats.avgWordLength * 0.3) +
    (stats.chars / 100 * 0.4) +
    (stats.sentences / 5 * 0.3);

  if (complexityScore < 2) return 'easy';
  if (complexityScore < 4) return 'medium';
  return 'hard';
};